//Vowel or Consonant

public class type1problem4{
    public static void main(String [] args){
      char ch = 'I';
      if(ch=='a' || ch=='e' || ch=='i' || ch=='o' || ch=='u' 
      || ch=='A' || ch=='A' || ch=='I' || ch=='O' || ch=='U' ){
          System.out.println(ch + " is vowel");
      }else{
          System.out.println(ch + " is consonant");
      }
    }
}
